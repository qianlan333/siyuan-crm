from __future__ import annotations

import argparse
import json
import subprocess
from dataclasses import dataclass
from datetime import datetime, timezone
from pathlib import Path


ROOT = Path(__file__).resolve().parents[1]
REPORT_VERSION = "1"
EXPERIMENT_PREFIX = "experiments/ai_crm_next/"
ARCHIVE_PREFIX = "docs/archive/experiments_ai_crm_next/"
REFERENCE_SCAN_EXTENSIONS = {".md", ".py", ".sh", ".toml", ".yaml", ".yml"}
SELF_REFERENCE_PATHS = {
    "tests/test_experiments_inventory_report.py",
    "tools/report_experiments_inventory.py",
}


@dataclass(frozen=True)
class InventoryRecord:
    path: str
    area: str
    classification: str
    reason: str

    def as_dict(self) -> dict[str, str]:
        return {
            "path": self.path,
            "area": self.area,
            "classification": self.classification,
            "reason": self.reason,
        }


@dataclass(frozen=True)
class RootReference:
    path: str
    line: int
    classification: str
    evidence: str

    def as_dict(self) -> dict[str, object]:
        return {
            "path": self.path,
            "line": self.line,
            "classification": self.classification,
            "evidence": self.evidence,
        }


def build_report(root: Path = ROOT, *, generated_at: str | None = None) -> dict[str, object]:
    root = root.resolve()
    tracked = _tracked_or_discovered_files(root)
    experiment_paths = sorted(path for path in tracked if path.startswith(EXPERIMENT_PREFIX))
    archived_paths = sorted(path for path in tracked if path.startswith(ARCHIVE_PREFIX))
    records = [_classify_experiment_file(path) for path in experiment_paths]
    root_references = _root_references(root, tracked)

    area_counts: dict[str, int] = {}
    classification_counts: dict[str, int] = {}
    for record in records:
        area_counts[record.area] = area_counts.get(record.area, 0) + 1
        classification_counts[record.classification] = classification_counts.get(record.classification, 0) + 1

    archived_area_counts: dict[str, int] = {}
    for path in archived_paths:
        area = _archive_area(path)
        archived_area_counts[area] = archived_area_counts.get(area, 0) + 1

    return {
        "version": REPORT_VERSION,
        "root": ".",
        "generated_at": generated_at or _utc_now(),
        "summary": {
            "experiment_tracked_file_count": len(experiment_paths),
            "archived_experiment_file_count": len(archived_paths),
            "root_reference_count": len(root_references),
            "areas": dict(sorted(area_counts.items())),
            "classifications": dict(sorted(classification_counts.items())),
            "archived_areas": dict(sorted(archived_area_counts.items())),
        },
        "root_references": [reference.as_dict() for reference in root_references],
        "experiment_files": [record.as_dict() for record in records],
        "archived_files": archived_paths,
    }


def render_markdown(report: dict[str, object]) -> str:
    summary = report["summary"]
    records = list(report["experiment_files"])
    root_references = list(report["root_references"])
    archived_files = list(report["archived_files"])
    lines = [
        "# experiments/ai_crm_next Cleanup Inventory",
        "",
        f"Generated: {report['generated_at']}",
        "",
        "This report is generated by `tools/report_experiments_inventory.py`.",
        "It is read-only evidence for the retired `experiments/ai_crm_next` workspace;",
        "it does not delete files, change runtime imports, or authorize external calls.",
        "",
        "## Scope",
        "",
        "`experiments/ai_crm_next` is a retired experiment stub. The live runtime",
        "package is root `aicrm_next/`; this inventory distinguishes the remaining",
        "stub from historical archive evidence and workspace snapshots.",
        "",
        "Retention rule: keep only the stub required by the duplicate-source guard",
        "under `experiments/ai_crm_next/`. Historical canary, gray-release, rehearsal,",
        "signoff, execution evidence, and the final experiment-local test scaffold",
        "stay archived under `docs/archive/experiments_ai_crm_next/`.",
        "",
        "Do not introduce `experiments/ai_crm_next/src/aicrm_next`.",
        "",
        "## Top-Level Counts",
        "",
        "| area | tracked files | current classification |",
        "|---|---:|---|",
    ]
    area_labels = {
        "root_config": "retired experiment entry stub",
        "migrations": "experiment-local Alembic scaffold and PostgreSQL readiness migrations",
        "scripts": "experiment-local helper scripts",
        "tools": "experiment-local helper / evidence tooling",
        "tests": "experiment-local active regression coverage",
        "test_fixtures": "parity fixture data used by experiment-local tests",
        "docs": "active experiment reference docs",
        "unexpected": "needs manual review",
    }
    for area, count in sorted(summary["areas"].items()):
        lines.append(f"| `{area}` | {count} | {area_labels.get(area, 'tracked experiment file')} |")
    for area, count in sorted(summary["archived_areas"].items()):
        lines.append(f"| archive `{area}` | {count} | archived historical evidence |")
    lines.append(
        f"| tracked files under `experiments/ai_crm_next` | {summary['experiment_tracked_file_count']} | retired stub total |"
    )
    lines.extend(
        [
            "",
            "## Root References",
            "",
            "Current root-level references found outside `experiments/ai_crm_next`,",
            "`docs/archive/`, and `docs/cleanup/`:",
            "",
        ]
    )
    if root_references:
        lines.extend(["| root file | line | classification | evidence |", "|---|---:|---|---|"])
        for reference in root_references:
            lines.append(
                "| `{path}` | {line} | `{classification}` | {evidence} |".format(
                    path=reference["path"],
                    line=reference["line"],
                    classification=reference["classification"],
                    evidence=_markdown_table_text(str(reference["evidence"])),
                )
            )
    else:
        lines.append("No root references were found.")
    lines.extend(
        [
            "",
            "No root CI workflow or root runtime module imports experiment-local source files",
            "as live runtime code.",
            "",
            "## Active Experiment Files",
            "",
        ]
    )
    for area in ("root_config", "migrations", "scripts", "tools", "tests", "test_fixtures", "docs", "unexpected"):
        subset = [record for record in records if record["area"] == area]
        if not subset:
            continue
        lines.extend([f"### {area}", "", "| path | classification | reason |", "|---|---|---|"])
        for record in subset:
            lines.append(
                "| `{path}` | `{classification}` | {reason} |".format(
                    path=record["path"],
                    classification=record["classification"],
                    reason=_markdown_table_text(record["reason"]),
                )
            )
        lines.append("")
    lines.extend(
        [
            "## Archived Historical Evidence",
            "",
            f"{len(archived_files)} archived files are currently retained under",
            "`docs/archive/experiments_ai_crm_next/`.",
            "",
            "Archived evidence includes historical canary, gray-release, rehearsal, signoff,",
            "execution, risk, and production-canary planning records. Do not delete these",
            "directly; move or remove them only after a separate audit/evidence retention",
            "decision.",
            "",
            "## Cleanup Order Recommendation",
            "",
            "1. Keep the root duplicate-source guard and retired experiment stub.",
            "2. Keep archived workspace snapshots under `docs/archive/experiments_ai_crm_next/`.",
            "3. Do not restore experiment-local tests/tools as an alternate runtime path.",
            "4. Replace manual inventory edits with this generated report.",
            "5. Consider only archive-retention decisions in future cleanup batches.",
            "",
            "## Non-Goals",
            "",
            "- No runtime code changes.",
            "- No deploy/nginx/systemd changes.",
            "- No external calls.",
            "- No production data access.",
            "- No deletion of evidence records in this batch.",
            "",
        ]
    )
    return "\n".join(lines)


def write_report_files(report: dict[str, object], *, summary_output: Path | None = None, json_output: Path | None = None) -> None:
    if summary_output is not None:
        summary_output.parent.mkdir(parents=True, exist_ok=True)
        summary_output.write_text(render_markdown(report), encoding="utf-8")
    if json_output is not None:
        json_output.parent.mkdir(parents=True, exist_ok=True)
        json_output.write_text(json.dumps(report, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")


def main(argv: list[str] | None = None) -> int:
    parser = argparse.ArgumentParser(description="Report experiments/ai_crm_next cleanup inventory without runtime changes.")
    parser.add_argument("--root", default=str(ROOT))
    parser.add_argument("--json-output")
    parser.add_argument("--summary-output")
    parser.add_argument("--generated-at", help="Override generated_at for reproducible reports.")
    args = parser.parse_args(argv)

    root = Path(args.root).resolve()
    report = build_report(root, generated_at=args.generated_at)
    write_report_files(
        report,
        summary_output=(root / args.summary_output) if args.summary_output else None,
        json_output=(root / args.json_output) if args.json_output else None,
    )
    print(render_markdown(report))
    print(json.dumps(report, ensure_ascii=False, indent=2))
    return 0


def _tracked_or_discovered_files(root: Path) -> list[str]:
    tracked = _git_ls_files(root)
    if tracked:
        return tracked
    return sorted(
        str(path.relative_to(root))
        for path in root.rglob("*")
        if path.is_file() and ".git" not in path.relative_to(root).parts
    )


def _git_ls_files(root: Path) -> list[str]:
    try:
        result = subprocess.run(
            ["git", "ls-files"],
            cwd=root,
            check=False,
            capture_output=True,
            text=True,
        )
    except OSError:
        return []
    if result.returncode != 0:
        return []
    return sorted(line for line in result.stdout.splitlines() if line)


def _classify_experiment_file(path: str) -> InventoryRecord:
    relative = path.removeprefix(EXPERIMENT_PREFIX)
    parts = relative.split("/")
    if len(parts) == 1:
        return InventoryRecord(path, "root_config", "retired_stub", "Retired experiment entry stub.")
    if parts[0] == "migrations":
        return InventoryRecord(path, "migrations", "active_experiment_scaffold", "Experiment-local migration scaffold/readiness coverage.")
    if parts[0] == "scripts":
        return InventoryRecord(path, "scripts", "active_experiment_helper", "Experiment-local helper script.")
    if parts[0] == "tools":
        if parts[-1].startswith("check_"):
            return InventoryRecord(path, "tools", "active_readiness_checker", "Paired with experiment-local readiness tests or archived evidence.")
        if parts[-1] == "doc_paths.py":
            return InventoryRecord(path, "tools", "active_archive_path_helper", "Keeps experiment-local tests pointed at archived evidence docs.")
        return InventoryRecord(path, "tools", "active_experiment_helper", "Experiment-local parity/evidence helper.")
    if parts[0] == "tests":
        if "fixtures" in parts:
            return InventoryRecord(path, "test_fixtures", "active_fixture_data", "Parity fixture data used by experiment-local tests.")
        return InventoryRecord(path, "tests", "active_regression_test", "Experiment-local regression or evidence-readiness test.")
    if parts[0] == "docs":
        return InventoryRecord(path, "docs", "active_reference_doc", "Reference material kept while the frozen workspace remains discoverable.")
    return InventoryRecord(path, "unexpected", "needs_manual_review", "Tracked experiment file outside known frozen-workspace areas.")


def _root_references(root: Path, tracked: list[str]) -> list[RootReference]:
    references: list[RootReference] = []
    for path in tracked:
        candidate = Path(path)
        if path in SELF_REFERENCE_PATHS or path.startswith((EXPERIMENT_PREFIX, ARCHIVE_PREFIX, "docs/cleanup/")):
            continue
        if candidate.suffix not in REFERENCE_SCAN_EXTENSIONS:
            continue
        full_path = root / candidate
        if not full_path.is_file():
            continue
        for line_number, line in enumerate(full_path.read_text(encoding="utf-8", errors="ignore").splitlines(), start=1):
            if "experiments/ai_crm_next" not in line:
                continue
            references.append(
                RootReference(
                    path=path,
                    line=line_number,
                    classification=_classify_root_reference(path, line),
                    evidence=line.strip(),
                )
            )
    return sorted(references, key=lambda item: (item.path, item.line, item.evidence))


def _classify_root_reference(path: str, line: str) -> str:
    if path == "README.md":
        return "active_doc_pointer"
    if path.startswith("tests/"):
        return "active_guard_test"
    if "src/aicrm_next" in line or "duplicate" in line.lower():
        return "active_duplicate_source_guard"
    if path.startswith("scripts/"):
        return "active_guard_script"
    return "manual_review"


def _archive_area(path: str) -> str:
    relative = path.removeprefix(ARCHIVE_PREFIX)
    parts = relative.split("/")
    if len(parts) == 1:
        return "root_archive_doc"
    return parts[0]


def _markdown_table_text(value: str) -> str:
    return value.replace("|", "\\|")


def _utc_now() -> str:
    return datetime.now(timezone.utc).replace(microsecond=0).isoformat().replace("+00:00", "Z")


if __name__ == "__main__":
    raise SystemExit(main())
